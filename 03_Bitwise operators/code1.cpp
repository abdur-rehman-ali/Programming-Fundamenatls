
//Bitwise operators
//1. AND (&)
//1. OR (|)
//1. NOT (~)
//1. XOR (^) (Two different = output 1)

#include<iostream>
using namespace std;

int main()
{
    int a = 4;
    int b = 5;

    cout<<"(a&b) "<<(a&b)<<endl;
    cout<<"(a|b) "<<(a|b)<<endl;
    cout<<"(~a) "<<(~a)<<endl;
    cout<<"(a^b) "<<(a^b)<<endl;
}